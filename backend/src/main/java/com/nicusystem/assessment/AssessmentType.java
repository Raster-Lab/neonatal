package com.nicusystem.assessment;

/**
 * Enumeration of neonatal assessment types.
 */
public enum AssessmentType {

    /** Comprehensive assessment performed upon NICU admission. */
    ADMISSION,

    /** Nursing assessment performed at each shift change. */
    SHIFT,

    /** Assessment performed during daily rounds. */
    DAILY_ROUND,

    /** Assessment performed at patient discharge. */
    DISCHARGE
}
