package com.nicusystem.consent;

/**
 * Types of patient consents tracked in the NICU.
 */
public enum ConsentType {
    /** General treatment consent. */
    TREATMENT,
    /** Photography consent. */
    PHOTOGRAPHY,
    /** Research participation consent. */
    RESEARCH_PARTICIPATION,
    /** Surgical procedure consent. */
    SURGERY,
    /** Blood transfusion consent. */
    BLOOD_TRANSFUSION,
    /** Autopsy consent. */
    AUTOPSY
}
