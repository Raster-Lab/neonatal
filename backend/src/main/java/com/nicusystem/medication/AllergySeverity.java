package com.nicusystem.medication;

/**
 * Severity levels for drug allergies.
 */
public enum AllergySeverity {
    /** Mild allergic reaction with minor symptoms. */
    MILD,
    /** Moderate allergic reaction requiring medical attention. */
    MODERATE,
    /** Severe allergic reaction with significant symptoms. */
    SEVERE,
    /** Life-threatening allergic reaction such as anaphylaxis. */
    LIFE_THREATENING
}
