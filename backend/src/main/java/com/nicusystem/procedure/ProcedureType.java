package com.nicusystem.procedure;

/**
 * Enumeration of procedure types for neonatal procedure documentation.
 */
public enum ProcedureType {
    /** Endotracheal intubation. */
    INTUBATION,
    /** Central or peripheral line placement. */
    LINE_PLACEMENT,
    /** Lumbar puncture (spinal tap). */
    LUMBAR_PUNCTURE,
    /** Umbilical arterial or venous line insertion. */
    UMBILICAL_LINE,
    /** Chest tube insertion for pneumothorax or effusion. */
    CHEST_TUBE,
    /** Exchange transfusion for hyperbilirubinemia or polycythemia. */
    EXCHANGE_TRANSFUSION
}
