package com.nicusystem.vitals;

/**
 * Body sites for temperature measurement.
 */
public enum TemperatureSite {
    /** Axillary (armpit) measurement. */
    AXILLARY,
    /** Rectal measurement. */
    RECTAL,
    /** Skin surface measurement. */
    SKIN,
    /** Core body temperature. */
    CORE
}
